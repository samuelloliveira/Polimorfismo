package polimorfismo;

public class Matematica {
	
	
	// Métodos para somar 3 tipos de numeros.
	public int somar(int num1, int num2) {
		return num1 + num2;
	}
	
	public double somar(double num1, double num2) {
		return num1 + num2;
	}
	
	public float somar(float num1, float num2) {
		return num1 + num2;
	}
	
	
	// Métodos para diminuir 3 tipos de numeros.
	public int subtrair(int num1, int num2) {
		return num1 - num2;
	}
	
	public double subtrair(double num1, double num2) {
		return num1 - num2;
	}
	
	public float subtrair(float num1, float num2) {
		return num1 - num2;
	}
	
	
	// Métodos para multiplicar 3 tipos de numeros.
	public int multiplicar(int num1, int num2) {
		return num1 * num2;
	}
	
	public double multiplicar(double num1, double num2) {
		return num1 * num2;
	}
	
	public float multiplicar(float num1, float num2) {
		return num1 * num2;
	}
	
	
	// Métodos para dividir 3 tipos de numeros.
	public int dividir(int num1, int num2) {

		return num1 / num2;
	}
	
	public double dividir(double num1, double num2) {

		return num1 / num2;
	}
	
	public float dividir(float num1, float num2) {

		return num1 / num2;
	}

}
